        public override string[] GetRolesForUser(string username)
        {
            using (var _Context = new NotesEntities())
            {
                var userrole = (from user in _Context.Users
                              join role in _Context.UserRoles on user.RoleID equals role.ID
                              where user.ID.ToString() == username
                              select role.Name).ToArray();
                return userrole;
            }

